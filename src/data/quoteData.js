export const getQuoteById = (id) => {
  return quotes.find(quote => quote.id === parseInt(id))
}

export const getQuotesByPage = (page) => {
  return quotes.filter(quote => quote.page === page)
}

export const getTotalPages = () => {
  return Math.max(...quotes.map(q => q.page))
}